// Funzione per trovare l'Angelo in base alla data di nascita
export const findAngelByDate = (month, day) => {
  const dateStr = `${String(month).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
  
  return angels72Database.find(angel => {
    const start = angel.dateStart;
    const end = angel.dateEnd;
    
    // Gestisce il caso in cui il periodo attraversa il cambio d'anno
    if (start <= end) {
      return dateStr >= start && dateStr <= end;
    } else {
      // Periodo che attraversa il cambio d'anno (es. 12-27 to 01-05)
      return dateStr >= start || dateStr <= end;
    }
  });
};

// Funzione per ottenere il contenuto dell'Angelo in base al piano
export const getAngelContent = (angelNum, planType = 'free') => {
  const angel = angels72Database.find(a => a.num === angelNum);
  if (!angel) return null;
  return planContents[planType](angel);
};

// Funzione per ottenere tutti gli angeli di un coro specifico
export const getAngelsByCoro = (coro) => {
  return angels72Database.filter(angel => angel.coro === coro);
};

// Funzione per ottenere tutti gli angeli di un elemento specifico
export const getAngelsByElemento = (elemento) => {
  return angels72Database.filter(angel => angel.elemento === elemento);
};

// Funzione per ottenere tutti gli angeli di un arcangelo specifico
export const getAngelsByArcangelo = (arcangelo) => {
  return angels72Database.filter(angel => angel.arcangelo === arcangelo);
};

// Funzione per cercare angeli per nome (ricerca parziale)
export const searchAngelsByName = (query) => {
  const searchTerm = query.toLowerCase();
  return angels72Database.filter(angel => 
    angel.nome.toLowerCase().includes(searchTerm)
  );
};

// Funzione per ottenere statistiche del database
