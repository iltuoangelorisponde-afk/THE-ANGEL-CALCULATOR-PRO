// Funzione per trovare l'Angelo in base alla data di nascita
export const findAngelByDate = (month, day) => {
  const dateStr = `${String(month).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
  
  return angels72Database.find(angel => {
    const start = angel.dateStart;
    const end = angel.dateEnd;
    
    // Gestisce il caso in cui il periodo attraversa il cambio d'anno
    if (start <= end) {
      return dateStr >= start && dateStr <= end;
    } else {
      // Periodo che attraversa il cambio d'anno (es. 12-27 to 01-05)
      return dateStr >= start || dateStr <= end;
    }
  });
};

// Funzione per ottenere il contenuto dell'Angelo in base al piano
export const getAngelContent = (angelNum, planType = 'free') => {
  const angel = angels72Database.find(a => a.num === angelNum);
  if (!angel) return null;
  return planContents[planType](angel);
};

