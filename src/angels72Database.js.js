// Funzione per ottenere tutti gli angeli di un arcangelo specifico
export const getAngelsByArcangelo = (arcangelo) => {
  return angels72Database.filter(angel => angel.arcangelo === arcangelo);
};

// Funzione per cercare angeli per nome (ricerca parziale)
export const searchAngelsByName = (query) => {
  const searchTerm = query.toLowerCase();
  return angels72Database.filter(angel => 
    angel.nome.toLowerCase().includes(searchTerm)
  );
};

