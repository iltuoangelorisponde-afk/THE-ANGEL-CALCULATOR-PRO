export function findAngelByDate(month, day, lang = 'IT') {
  let angels;
  switch (lang) {
    case 'EN': angels = ANGELS_EN; break;
    case 'FR': angels = ANGELS_FR; break;
    default: angels = ANGELS_IT;
  }
  const dateCode = month.toString().padStart(2, "0") + "-" + day.toString().padStart(2, "0");
  const targetInt = parseInt(dateCode.replace("-", ""));
  for (let angel of angels) {
    const fromInt = parseInt(angel.from.replace("-", ""));
    const toInt = parseInt(angel.to.replace("-", ""));
    if (fromInt <= toInt) {
      if (targetInt >= fromInt && targetInt <= toInt) return angel;
    } else {
      if (targetInt >= fromInt || targetInt <= toInt) return angel;
    }
  }
  return null;
}
