export const findAngelByDate = (month, day) => {
  const dateStr = `${String(month).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
  
  return angels72Database.find(angel => {
    const start = angel.dateStart;
    const end = angel.dateEnd;
    
    if (start <= end) {
      return dateStr >= start && dateStr <= end;
    } else {
      return dateStr >= start || dateStr <= end;
    }
  });
};

